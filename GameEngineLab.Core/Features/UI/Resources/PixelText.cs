public static class PixelText
{
    public static void Draw(SpriteBatch spriteBatch, Texture2D pixel, string text, Vector2 position, int scale, Color color, int letterSpacing = 1)
    {
        var cursor = position;
        var upper = text.ToUpperInvariant();

        foreach (var ch in upper)
        {
            if (!Glyphs.TryGetValue(ch, out var glyph))
            {
                glyph = Glyphs['?'];
            }

            for (var row = 0; row < glyph.Length; row++)
            {
                for (var col = 0; col < glyph[row].Length; col++)
                {
                    if (glyph[row][col] != '1')
                    {
                        continue;
                    }

                    spriteBatch.Draw(pixel, new Rectangle((int)cursor.X + (col * scale), (int)cursor.Y + (row * scale), scale, scale), color);
                }
            }

            cursor.X += (glyph[0].Length * scale) + letterSpacing;
        }
    }

    public static Point Measure(string text, int scale, int letterSpacing = 1)
    {
        var width = 0;
        var height = 7 * scale;

        foreach (var ch in text.ToUpperInvariant())
        {
            if (!Glyphs.TryGetValue(ch, out var glyph))
            {
                glyph = Glyphs['?'];
            }

            width += (glyph[0].Length * scale) + letterSpacing;
        }

        return new Point(width > 0 ? width - letterSpacing : 0, height);
    }

